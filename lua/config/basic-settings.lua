-- Базовые настройки
vim.opt.number = true               -- номера строк
vim.opt.relativenumber = true       -- относительные номера строк
vim.opt.tabstop = 2                 -- 2 пробела на таб
vim.opt.softtabstop = 2
vim.opt.shiftwidth = 2              -- 2 пробела для автоотступов
vim.opt.expandtab = true            -- использовать пробелы вместо табов
vim.opt.smartindent = true          -- умные отступы
vim.opt.syntax = "enable"           -- подсветка синтаксиса
vim.opt.scrolloff = 8               -- автопрокрутка
vim.opt.clipboard = "unnamedplus"   -- вставка
